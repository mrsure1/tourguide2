# 이 함수는 생성된 FAQ 데이터가 실제로 50개인지, 그리고 필요한 컬럼이 빠지지 않았는지 점검합니다.
# 작은 실수를 초기에 잡아 주는 안전장치 역할을 합니다.
def validate_faq_records(records):
    if len(records) != 50:
        raise ValueError(f"FAQ 데이터 개수는 50개여야 합니다. 현재 개수: {len(records)}")

    required_keys = {"Question", "Answer"}
    for index, record in enumerate(records, start=1):
        if set(record.keys()) != required_keys:
            raise ValueError(f"{index}번째 데이터의 컬럼 구성이 올바르지 않습니다: {record}")


# 이 함수는 문답 리스트를 pandas 데이터프레임으로 바꾼 뒤 faq_data.csv 파일로 저장합니다.
# utf-8-sig 인코딩을 사용해 엑셀에서도 한글이 깨질 가능성을 줄였습니다.
def save_faq_csv(records, output_path):
    faq_df = pd.DataFrame(records, columns=["Question", "Answer"])
    faq_df.to_csv(output_path, index=False, encoding="utf-8-sig")


# 이 함수는 전체 실행 흐름을 담당합니다.
# 1) FAQ 데이터 생성 -> 2) 데이터 검증 -> 3) CSV 저장 순서로 작업합니다.
def main():
    output_file = Path("faq_data.csv")
    records = build_faq_records()
    validate_faq_records(records)
    save_faq_csv(records, output_file)
    print(f"총 {len(records)}개의 FAQ 데이터가 '{output_file}' 파일로 저장되었습니다.")


if __name__ == "__main__":
    main()
